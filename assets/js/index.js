function switchPhoto() {
    document.getElementById("sec-1").style.backgroundImage = "url('https://preview.colorlib.com/theme/gamewarrior/img/slider-2.jpg')";
    document.getElementById("sec-1").style.transition = "background-color 0.5s"
}

function revertPhoto() {
    document.getElementById("sec-1").style.backgroundImage = "url('https://preview.colorlib.com/theme/gamewarrior/img/slider-1.jpg')";
    document.getElementById("sec-1").style.transition = "background-color 0.5s"
} 

